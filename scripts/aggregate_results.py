#!/usr/bin/env python3
"""Aggregate DraCo eval results into a single Markdown summary.

Scans experiments/preds_<split>_<model>.json (and *.raw.jsonl checkpoints)
and prints a table with EM / ES / ID.EM / F1 plus completion progress.
"""
import argparse
import json
import os
import re
import subprocess
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
EXP_DIR = ROOT / "experiments"
RES_DIR = ROOT / "results"

# Expected sample counts for each split (used to compute "X / N" progress)
EXPECTED = {
    "repoeval_line": 1600,
    "repoeval_api": 1600,
    "repoeval_function": 455,
    "recceval": 6461,
    "cce_python": 2086,
}

DISPLAY_NAME = {
    "repoeval_line": "RepoEval Line",
    "repoeval_api": "RepoEval API",
    "repoeval_function": "RepoEval Function",
    "recceval": "ReccEval",
    "cce_python": "CCE-Python",
}


def parse_evaluator(path: Path) -> dict | None:
    """Run experiments/evaluator.py on a predictions file and parse the metrics."""
    try:
        out = subprocess.run(
            [sys.executable, str(ROOT / "experiments" / "evaluator.py"),
             "--path", str(path)],
            capture_output=True, text=True, check=True, timeout=600,
        ).stdout
    except (subprocess.CalledProcessError, subprocess.TimeoutExpired) as e:
        return {"error": str(e)}
    metrics = {}
    for key in ("EM", "ES", "ID.EM", "F1"):
        m = re.search(rf"^{re.escape(key)}:\s*([\d.]+)", out, re.MULTILINE)
        if m:
            metrics[key] = float(m.group(1))
    n = re.search(r"Num of test data:\s*(\d+)", out)
    if n:
        metrics["n"] = int(n.group(1))
    return metrics


def count_lines(path: Path) -> int:
    if not path.is_file():
        return 0
    with path.open("rb") as f:
        return sum(1 for _ in f)


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--model_key", default=os.environ.get("MODEL_KEY", "deepseekcoder6b7"))
    ap.add_argument("--out", default=None,
                    help="Output Markdown path (default: results/SUMMARY_<model>.md)")
    args = ap.parse_args()

    RES_DIR.mkdir(exist_ok=True)
    out_md = Path(args.out) if args.out else RES_DIR / f"SUMMARY_{args.model_key}.md"

    rows = []
    for key, expected in EXPECTED.items():
        pred = EXP_DIR / f"preds_{key}_{args.model_key}.json"
        raw = EXP_DIR / f"preds_{key}_{args.model_key}.json.raw.jsonl"
        # Also check src/experiments/ as a fallback (when scripts ran from src/)
        if not pred.is_file():
            alt = ROOT / "src" / "experiments" / pred.name
            if alt.is_file():
                pred = alt
                raw = ROOT / "src" / "experiments" / raw.name
        done = count_lines(raw)
        status = "—"
        metrics: dict = {}
        if pred.is_file():
            metrics = parse_evaluator(pred) or {}
            status = "done"
        elif done > 0:
            status = f"running ({done}/{expected})"
        rows.append({
            "key": key,
            "name": DISPLAY_NAME[key],
            "expected": expected,
            "done": done,
            "status": status,
            "metrics": metrics,
            "pred_path": pred if pred.is_file() else None,
        })

    # Build Markdown
    lines = []
    lines.append(f"# DraCo eval summary — `{args.model_key}`")
    lines.append("")
    lines.append("| Dataset | Status | Samples | EM | ES | ID.EM | F1 |")
    lines.append("|---|---|---:|---:|---:|---:|---:|")
    for r in rows:
        m = r["metrics"]
        n = m.get("n", r["done"] if r["status"] != "done" else r["expected"])
        em = f"{m['EM']:.2f}" if "EM" in m else "—"
        es = f"{m['ES']:.2f}" if "ES" in m else "—"
        idem = f"{m['ID.EM']:.2f}" if "ID.EM" in m else "—"
        f1 = f"{m['F1']:.2f}" if "F1" in m else "—"
        lines.append(f"| {r['name']} | {r['status']} | {n}/{r['expected']} | {em} | {es} | {idem} | {f1} |")
    lines.append("")
    completed = sum(1 for r in rows if r["status"] == "done")
    lines.append(f"**Completed splits: {completed} / {len(rows)}**")
    lines.append("")
    lines.append("Generated by `scripts/aggregate_results.py`.")

    out_md.write_text("\n".join(lines) + "\n")
    print("\n".join(lines))
    print(f"\n[aggregate] Wrote {out_md}")


if __name__ == "__main__":
    main()
